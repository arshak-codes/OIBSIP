private void initTrainInfoMap() {
    trainInfoMap = new HashMap<>();
    trainInfoMap.put("101", "Malabar Exp");
    trainInfoMap.put("102", "Rajadhani Exp");
    trainInfoMap.put("103", "Shatabdi Express");
    trainInfoMap.put("104", "Duronto Express");
    trainInfoMap.put("105", "Gatimaan Express");
    trainInfoMap.put("106", "Tejas Express");
    // Add more train number and name pairs as needed
}

private void updateTrainNameField() {
    String trainNumber = trainnumfield.getText();
    String trainName = trainInfoMap.get(trainNumber);

    if (trainName != null) {
        jTextField5.setText(trainName);
        
        // Set fare based on train number
        if (trainNumber.equals("101")) {
            farefield.setText("120");
        } else if (trainNumber.equals("102")) {
            farefield.setText("200");
        } else if (trainNumber.equals("103")) {
            farefield.setText("130");
        } else if (trainNumber.equals("104")) {
            farefield.setText("150");
        } else if (trainNumber.equals("105")) {
            farefield.setText("200");
        } else if (trainNumber.equals("106")) {
            farefield.setText("250");
        } else {
            farefield.setText("");
        }
    } else {
        jTextField5.setText("Train not found");
        farefield.setText("");
    }
    
}

