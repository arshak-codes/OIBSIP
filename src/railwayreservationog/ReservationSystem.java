    private void addtrainbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addtrainbuttonActionPerformed
                                             
    if(trainnumfield.getText().equals("") || nameField.getText().equals("") || phn.getText().equals("")) {
        JOptionPane.showMessageDialog(this,"Please provide name, train number and phone no");
    } else {
        String trainNumber = trainnumfield.getText();
        Tname = trainInfoMap.get(trainNumber);
        PName = nameField.getText();
        Pnr =  "PNR";
        Random random = new Random();
        for (int i = 0; i < 6; i++) {
            Pnr+=random.nextInt(10); // Append a random digit (0-9)
        }
        
        String start = (String) jComboBox1.getSelectedItem();
        String dest = (String) jComboBox3.getSelectedItem();
        String price = farefield.getText();
        
        try {
            // Assuming 'con' is your Connection object
            PreparedStatement pst = con.prepareStatement("INSERT INTO traindetails(pnr, tname, start, dest, price) VALUES (?, ?, ?, ?, ?)");

            pst.setString(1, Pnr);
            pst.setString(2, Tname);
            pst.setString(3, start);
            pst.setString(4, dest);
            pst.setString(5, price);

            int k = pst.executeUpdate();

            if(k == 1) {
                JOptionPane.showMessageDialog(this, "Booking Successful! Your ticket is confirmed.");
                Bookedframe bf = new Bookedframe(PName, Pnr, Tname);
                this.hide();
                bf.setReservationSystem(this);
                bf.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Booking Failed! ☹️");
            }
           
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "An error occurred while booking your ticket. Please try again later.");
        }
    }


    }//GEN-LAST:event_addtrainbuttonActionPerformed

